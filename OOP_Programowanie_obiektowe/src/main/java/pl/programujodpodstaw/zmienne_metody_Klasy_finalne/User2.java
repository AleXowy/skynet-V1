package pl.programujodpodstaw.zmienne_metody_Klasy_finalne;

public final class User2 {                                                                                              // FINAL w klasie oznacza że nie mozemy tej kalsy rozszerzyć (dziedziczyć) tworzyć klas potomnych (inherit)
                                                                                                                        // tworze metdoę, którą napisuje (Overloading) w Klasie Mod.java
public final void  hello () {                                                                                           // FINAL zastosowany w metodzie sprawia że nie możemy nadpisac (overloading) w metodzie potomnej (dziedziczenie) możemy dziedziczyć, ale nie możemy nic zmienić
    System.out.println("Jestem user2");
}
}
